    this.submittedAt,
    this.verifiedAt,
    this.approvedAt,
    this.certificate,
  });

  PipelineAssignment copyWith({
    PipelineAssignmentStatus? status,
    PipelineWorkSubmission? submission,
    String? reviewerNotes,
    String? adminNotes,
    DateTime? verifiedAt,
    DateTime? approvedAt,
    PipelineCertificate? certificate,
  }) => PipelineAssignment(
    id: id,
    activityId: activityId,
    eventId: eventId,
    studentName: studentName,
    studentEmail: studentEmail,
    activityTitle: activityTitle,
    eventTitle: eventTitle,
    dueDate: dueDate,
