    submittedAt: submittedAt,
    verifiedAt: verifiedAt ?? this.verifiedAt,
    approvedAt: approvedAt ?? this.approvedAt,
    certificate: certificate ?? this.certificate,
  );

  String get initials {
    final parts = studentName.trim().split(' ');
    if (parts.length >= 2) return '${parts[0][0]}${parts[1][0]}'.toUpperCase();
    return studentName.isNotEmpty ? studentName[0].toUpperCase() : '?';
  }
}

class PipelineActivity {
  final int id;
  final int eventId;
  final String title;
  final ActivityRole role;
  final String? description;
  final int maxStudents;
  final bool requiresDonationProof;
  final bool hasCertificate;
  final List<PipelineAssignment> assignments;

  const PipelineActivity({
    required this.id,
    required this.eventId,
    required this.title,
    required this.role,
    this.description,
    required this.maxStudents,
    this.requiresDonationProof = false,
    this.hasCertificate = true,
    this.assignments = const [],
  });

  int get assignedCount => assignments.length;
  bool get isFull => assignedCount >= maxStudents;
  int get slotsLeft => maxStudents - assignedCount;
  int get pendingSubmissions => assignments.where((a) => a.status == PipelineAssignmentStatus.submitted).length;
  int get verifiedCount => assignments.where((a) => a.status == PipelineAssignmentStatus.eventManagerVerified || a.status == PipelineAssignmentStatus.adminApproved || a.status == PipelineAssignmentStatus.certificateGenerated || a.status == PipelineAssignmentStatus.completed).length;

  PipelineActivity copyWith({List<PipelineAssignment>? assignments}) => PipelineActivity(
    id: id,
    eventId: eventId,
    title: title,
    role: role,
    description: description,
    maxStudents: maxStudents,
    requiresDonationProof: requiresDonationProof,
    hasCertificate: hasCertificate,
    assignments: assignments ?? this.assignments,
  );
}

class PipelineEvent {
  final int id;
  final String title;
  final EventCategory category;
  final PipelineEventStatus status;
  final DateTime date;
  final DateTime? endDate;
  final String location;
  final bool isOnline;
  final String? partnerSchool;
  final String description;
  final String? bannerImageUrl;
  final int maxVolunteers;
  final String assignedEventManagerName;
  final String? assignedCounsellorName;
  final bool certificateEligible;
  final bool donationEligible;
  final bool stipendEligible;
  final double? stipendAmount;
  final String? expectedWork;
  final String? proofRequired;
  final bool reportRequired;
  final bool impactPostEligible;
  final List<PipelineActivity> activities;
  final DateTime createdAt;
  final ImpactPostDraft? impactDraft;
  final EventReport? report;

  const PipelineEvent({
    required this.id,
    required this.title,
    required this.category,
    required this.status,
    required this.date,
    this.endDate,
    required this.location,
    this.isOnline = false,
    this.partnerSchool,
    required this.description,
    this.bannerImageUrl,
    required this.maxVolunteers,
    required this.assignedEventManagerName,
    this.assignedCounsellorName,
    this.certificateEligible = true,
    this.donationEligible = false,
    this.stipendEligible = false,
    this.stipendAmount,
    this.expectedWork,
    this.proofRequired,
    this.reportRequired = true,
    this.impactPostEligible = true,
    this.activities = const [],
    required this.createdAt,
    this.impactDraft,
    this.report,
  });

  int get totalAssigned => activities.fold(0, (sum, a) => sum + a.assignedCount);
  int get totalSubmitted => activities.fold(0, (sum, a) => sum + a.assignments.where((s) => s.status != PipelineAssignmentStatus.open && s.status != PipelineAssignmentStatus.assigned && s.status != PipelineAssignmentStatus.inProgress).length);
  int get totalApproved => activities.fold(0, (sum, a) => sum + a.assignments.where((s) => s.status == PipelineAssignmentStatus.adminApproved || s.status == PipelineAssignmentStatus.certificateGenerated || s.status == PipelineAssignmentStatus.completed).length);
  int get pendingSubmissions => activities.fold(0, (sum, a) => sum + a.pendingSubmissions);
  int get totalPeopleReached => activities.fold(0, (sum, a) => sum + a.assignments.fold(0, (s2, asgn) => s2 + (asgn.submission?.peopleReached ?? 0)));
  double get totalDonationCollected => activities.fold(0.0, (sum, a) => sum + a.assignments.fold(0.0, (s2, asgn) => s2 + (asgn.submission?.donationProof?.amount ?? 0.0)));
  int get certificatesGenerated => activities.fold(0, (sum, a) => sum + a.assignments.where((s) => s.certificate != null).length);
  bool get hasImpactDraft => impactDraft != null;
  bool get hasReport => report != null;

  List<PipelineAssignment> get allAssignments => activities.expand((a) => a.assignments).toList();
  List<PipelineAssignment> get pendingEmReview => allAssignments.where((a) => a.status == PipelineAssignmentStatus.submitted).toList();

  bool get isToday {
    final now = DateTime.now();
    return date.year == now.year && date.month == now.month && date.day == now.day;
  }

  bool get isUpcoming => date.isAfter(DateTime.now());

  PipelineEvent copyWith({
    PipelineEventStatus? status,
    List<PipelineActivity>? activities,
    ImpactPostDraft? impactDraft,
    EventReport? report,
  }) => PipelineEvent(
    id: id,
    title: title,
    category: category,
    status: status ?? this.status,
    date: date,
    endDate: endDate,
    location: location,
    isOnline: isOnline,
    partnerSchool: partnerSchool,
    description: description,
    bannerImageUrl: bannerImageUrl,
    maxVolunteers: maxVolunteers,
    assignedEventManagerName: assignedEventManagerName,
    assignedCounsellorName: assignedCounsellorName,
    certificateEligible: certificateEligible,
    donationEligible: donationEligible,
    stipendEligible: stipendEligible,
    stipendAmount: stipendAmount,
    expectedWork: expectedWork,
    proofRequired: proofRequired,
    reportRequired: reportRequired,
    impactPostEligible: impactPostEligible,
    activities: activities ?? this.activities,
    createdAt: createdAt,
    impactDraft: impactDraft ?? this.impactDraft,
    report: report ?? this.report,
  );
}

