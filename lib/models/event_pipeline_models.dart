// ─── Pipeline Event Status ────────────────────────────────────────────────────

enum PipelineEventStatus {
  draft('Draft', Color(0xFF757575), Icons.edit_outlined),
  published('Published', Color(0xFF1565C0), Icons.public_rounded),
  registrationOpen('Registration Open', Color(0xFF2E7D32), Icons.app_registration_rounded),
  volunteerAssignment('Volunteer Assignment', Color(0xFF6A1B9A), Icons.assignment_ind_rounded),
  scheduled('Scheduled', Color(0xFFE65100), Icons.event_rounded),
  ongoing('Ongoing', Color(0xFFFF6F00), Icons.play_circle_rounded),
  workSubmissionOpen('Work Submission Open', Color(0xFF00695C), Icons.upload_file_rounded),
  verificationPending('Verification Pending', Color(0xFFF57F17), Icons.pending_actions_rounded),
  adminApprovalPending('Admin Approval Pending', Color(0xFFC62828), Icons.admin_panel_settings_rounded),
  completed('Completed', Color(0xFF4CAF50), Icons.check_circle_rounded),
  impactPublished('Impact Published', Color(0xFF1B5E20), Icons.newspaper_rounded),
  archived('Archived', Color(0xFF9E9E9E), Icons.archive_rounded);

  const PipelineEventStatus(this.label, this.color, this.icon);
  final String label;
  final Color color;
  final IconData icon;

  bool get isActive => this == ongoing || this == workSubmissionOpen || this == verificationPending || this == adminApprovalPending;
  bool get isPreEvent => this == draft || this == published || this == registrationOpen || this == volunteerAssignment || this == scheduled;
  bool get isPostEvent => this == completed || this == impactPublished || this == archived;

  static PipelineEventStatus fromString(String v) => PipelineEventStatus.values.firstWhere(
    (e) => e.name == v || e.label.toLowerCase() == v.toLowerCase(),
    orElse: () => PipelineEventStatus.draft,
  );

  // All 12 stages in order for the pipeline progress timeline
  static const List<PipelineEventStatus> pipeline = [
    draft, published, registrationOpen, volunteerAssignment,
    scheduled, ongoing, workSubmissionOpen, verificationPending,
    adminApprovalPending, completed, impactPublished, archived,
  ];

  int get pipelineIndex => pipeline.indexOf(this);
}

